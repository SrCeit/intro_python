#Operadores y sus variantes

#Suma
x = 5
x += 3
print(x)

x = 5
x = x + 3
print(x)

#Resta
x = 5
x -= 3
print(x)

x = 5
x = x - 3
print(x)

#Multiplicación
x = 5
x *= 3
print(x)

x = 5
x = x * 3
print(x)

#Division
x = 5
x /= 3
print(x)

x = 5
x = x / 3
print(x)

#Modulo/resto de la división
x = 5
x %= 3
print(x)

x = 5
x = x % 3
print(x)

#Exponente
x = 5
x **= 3
print(x)

x = 5
x = x ** 3
print(x)

#Floor division (devuelve siempre un entero)
x = 5
x //= 3
print(x)

x = 5
x = x // 3
print(x)

x = 10
y = 12

# Mayor: x > y is False
print('x > y is',x>y)

# Menor: x < y is True
print('x < y is',x<y)

# Igual: x == y is False
print('x == y is',x==y)

# Distinto de: x != y is True
print('x != y is',x!=y)

# Mayor o igual: x >= y is False
print('x >= y is',x>=y)

# Menor o igual: x <= y is True
print('x <= y is',x<=y)

#Lógica
x = True
y = False

print('x and y is',x and y)

print('x or y is',x or y)

print('not x is',not x)